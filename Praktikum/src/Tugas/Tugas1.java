package Tugas;

public class Tugas1 {
	public int angka1;
	public int angka2;
	
	public int getAngka1() {
		return angka1;
	}
	public void getAngka1(int angka1) {
		this.angka1 = angka1;
	}
	public int getAngka2() {
		return angka2;
	}
	public void getAngka2(int angka1) {
		this.angka2 = angka2;
	}
	
	public int tambah() {
		return angka1 + angka2;
	}
	public int kurang() {
		return angka1 -  angka2;
	}
	public int kali() {
		return angka1 * angka2;
	}	
	public int bagi() {
		return angka1 / angka2;	
	}
}
